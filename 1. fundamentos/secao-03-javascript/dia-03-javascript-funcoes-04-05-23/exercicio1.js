let saldo;

function add(dinheiro) {
    return dinheiro + saldo;
}

function subtrair(dinheiro) {
    return dinheiro - saldo;
}

function multiplicar(dinheiro) {
    return dinheiro * saldo;
}

function dividir(dinheiro) {
    return dinheiro / saldo;
}

console.log(add);
console.log(subtrair);
console.log(multiplicar);
console.log(dividir);